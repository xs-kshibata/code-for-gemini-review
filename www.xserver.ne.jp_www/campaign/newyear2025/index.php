（２）個人情報の第三者提供について

以下の場合を除き、取得した個人情報を、予め本人の同意を得ることなく第三者に提供することはありません。
提供される個人情報は、必要となる最小限の個人情報のみとし、かつ使用範囲もその範囲に限定します。

・法令に基づく場合
・人の生命、身体又は財産の保護、公衆衛生の向上又は児童の健全な育成の推進のために必要であって、お客様の同意を得ることが困難な場合


（３）個人情報の委託について
　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　
当社は、業務を円滑に進める等の理由から、業務の一部を外部に委託する際に、個人情報を委託する場合があります。
ただし、委託先に開示するお客様の個人情報は、当該業務の委託に必要となる最小限の個人情報のみとし、かつ使用範囲もその範囲に限定します。
尚、委託先は、十分な個人情報の保護水準を満たしている委託先を選定し、安全管理が図られるよう、委託先に対する必要かつ適切な管理監督をいたします。


（４）第三者から収集する情報について
　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　
当社は広告の効果測定のため、第三者の運営するサイトやサービス、ツール等から、当サイトに訪れる前にクリックされている広告の情報（クリック日や広告掲載サイト・注文番号等）を取得し、お預かりしているお客様の個人情報と照合する場合があります。


（５）クッキーの使用について
　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　
当サイトでは、お客様により便利にご利用いただくため、クッキーという技術を使用しています。
クッキーを使用することで、ウェブサイトはお客様のコンピュータを識別できるようになりますが、お客様個人を特定できるものではありません。
また、お客様はブラウザの設定により、クッキーの受取りを拒否することができます。その場合、当サイトが提供するサービスを一部ご利用できない場合があります。


（６）ウェブビーコンの使用について
　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　
当サイトは、お客様へのサービス向上等に役立てるため、ウェブビーコンの技術を使用する場合がありますが、ウェブビーコンを使用することで、お客様の個人情報を取得することはありません。


（７）個人情報提供の任意性について
　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　　
当社に個人情報をご提供いただくことは任意ですが、所定の個人情報をご提供いただくことができない場合、当社のサービスを一部ご利用できませんので、予めご了承ください。


