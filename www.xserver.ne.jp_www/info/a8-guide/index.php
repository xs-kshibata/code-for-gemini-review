    $(window).scroll(function () {
        var s = $(this).scrollTop();
        var m = 300;
        if(s > m) {
            $('#pagetop-btn').fadeIn('slow');
        } else if(s < m) {
            $('#pagetop-btn').fadeOut('slow');
        }
    });
});
