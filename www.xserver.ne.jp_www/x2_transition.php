				$( 'img.srcset' ).each( function () {
					var $img = $( this );
					var src = $img.attr( 'src' );
					var name = src.substring( 0, src.lastIndexOf( '_' ) + 1 );
					var ex = src.substring( src.lastIndexOf( '.' ) );

					imageLoader.addFile( {
						src: name + suffix + ex,
						extra: $img
					} );
				} );

				var onLoadProgress = function ( e ) {
					var $img = e.target.extra;
					$img
